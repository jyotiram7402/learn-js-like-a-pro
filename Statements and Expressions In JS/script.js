//Example of an Expression

let x = 5 + 3; // 5 + 3 is an expression (produces value 8)
console.log(x); // Outputs: 8

//Example of a Statement

if (x > 5) {
  // if statement (performs action)
  console.log("X is greater than 5");
}
/*
Key Rule
Expressions return a value → Can be used inside another expression.
Statements do something → Cannot be used inside an expression.
*/
