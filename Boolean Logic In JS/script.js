/*
Boolean logic in JavaScript is based on true/false values and logical operators like &&, ||, and !. These operators help in decision-making and control flow.

1. Boolean Values
JavaScript has two boolean values:


true
false

You can get boolean values using the Boolean() function:

*/

console.log(Boolean(1)); // true
console.log(Boolean(0)); // false
console.log(Boolean("")); // false (empty string)
console.log(Boolean("Hi")); // true (non-empty string)
console.log(Boolean(null)); // false
